사냥 기간 동안, 이 끝내주는 약병들을 충분히 많이 준비해놨어. 내년 이맘때 쯤이면 왕에 필적할 정도의 환상적인 수집품들을 모을 수 있을거야!

그런데 하나를 잘못 설치한 것 같아... 이 근처인게 분명한데.


아냐, 안돼! 이 - 이건 - 사고 --- 아파, 너무 아 --_____
  

#{italic}#당신은 가루가 채워진 작은 약병들이 잔뜩 있는, 녹슨 상자를 발견했습니다. 이것들은 상당히 쓸만할 것 같습니다.#{normal}#]],
