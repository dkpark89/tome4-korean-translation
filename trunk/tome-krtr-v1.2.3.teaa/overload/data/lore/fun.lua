일반인들의 관심을 처리하는 방법이 없다는 것을 이쯤에서 눈치채셨겠지요. 왜냐하면 사령술사는 그런 게 필요 없으니까요. 그들은 파괴될 일만 남은 가축들입니다.]]
}

