- #YELLOW#정화의 불꽃   : #WHITE#'불 붙음' 효과를 파열시켜, 한번에 연소되도록 만듭니다.
- #YELLOW#열화          : #WHITE#열화 마법을 완벽히 터득하여, 완벽한 화염 면역조차도 꿰뚫을 수 있습니다.
