	lore = [[마법 사용자를 잡았다면, 반드시 살해해야만 한다. 그것도 잡은 즉시. 마법사를 포로로 삼는 것은 너무 위험성이 크기 때문이다.

일반적으로 마법 사용자를 죽이는 가장 바람직한 방법은, 신체의 모든 부분을 조각내버리는 것이다. 이 괴물들에게 있어, 죽은 자를 언데드로 되살리는 것은 그리 어렵지 않은 일이라는 것을 기억하라.

머리를 자르는 것은 간단하고 효과적이며, 특히 시간이 없을 때 자주 사용되는 방법이다. 하지만 가능하면 신체를 완전히 조각내어, 각각의 조각들을 따로 금속 상자에 넣고 버드나무로 봉하라. 이 방법이 가장 이상적인 살해방법이라는 사실은 변하지 않는다.

동물들이 시체를 먹게 만드는 방법도 가끔 사용되지만, 추천하지는 않는다. 최소한 그 마법에 오염된 시체를 먹은 동물들이 설사 증세를 겪게 될 것이며, 최악의 경우 마나에 오염된 동물들이 화염을 내뿜는 지옥의 개로 타락해버릴 수도 있기 때문이다.

마법사를 얼려서 죽이려는 것은 효과적이지 못하며, 마법사가 아니라도 빙결로 인해 죽지는 않는다. 만약 당신이 빙결로 인해 죽은 사람에 대한 이야기를 들은 적이 있다면, 단순한 소문이거나 죽은 척 한 것을 잘못 알아서 퍼진 이야기일 것이다.

마법사를 태워서 죽이는 것은 주로 교육수준이 낮은 시골 마을에서 자주 일어나지만, 상식적으로 자신이 잡혀서 화형당할 때 까지의 긴 시간 동안 도망치지 못하는 마법사는 없을 것이다. 화형은 나무를 준비하는 시간이 너무 오래 걸리며, 아무리 나무를 미리 준비했더라도 준비에 시간이 걸리는 것은 마찬가지이다. 주로 이 방식으로 화형당하는 사람들은 진짜 마법사들이 아닌, 마법에 동조하거나 마법과 관련된 물품을 만들던 사람들이다. 하지만 엄밀히 말하자면 이렇게 진짜 마법사가 아닌 사람을 화형시키는 것도 나쁘지 않은 방법인데, 이는 마법사에 대한 경각심을 심어줄 수 있는 좋은 본보기가 되기 때문이다.

만약 시간과 주변 상황이 허용된다면, 마법사를 죽이는 가장 좋은 방법은 아주 천천히 신체 일부분을 조금씩 잘라 죽이는 것이다. 이를 위해서는 튼튼한 밀실이 필요하며, 버드나무나 너도밤나무로 마법사의 손목이나 머리를 미리 봉인해둘 필요가 있다. 우선 마법사의 손가락과 발가락, 그리고 외부 기관들을 하나씩 천천히 잘라낸 뒤, 팔과 다리를 자르고, 마지막으로 몸을 반으로 갈라 장기를 적출하는 방법을 추천한다. 두개골을 여는 것은 추천하지 않는데, 머리 속에 살고 있는 악마가 도망칠 수도 있기 때문이다. 머리는 따로 금속 상자에 넣어, 다른 신체의 조각들과 함께 24 시간 동안 소각시켜야 한다. 이를 위해서는 아주 날카로운 날붙이가 필요하며, 이 '일' 을 하는 도중에는 여러 번 날을 다시 갈아야 한다. 마법사들이 자신의 뼈를 단단하게 만드는 것은 그들이 마지막으로 하는 저항이자 일종의 절차와도 같은 것이기 때문이다.
