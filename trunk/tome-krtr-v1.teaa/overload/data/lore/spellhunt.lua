일반적인 마법사들을 상대할 때 가장 중요한 것은 기동성이다. 그들이 공격용이건 방어용이건 주문을 영창하기 전에, 낼 수 있는 최대한의 속도로 돌진하여 마법사의 머리를 쪼개버려야 한다. 만약 다수의 마법사와 상대해야 한다면 이 전략은 사용하기 힘들기 때문에, 일단 개방된 곳에서는 후퇴하는 것이 유효하다. 이것은 겁쟁이들의 도주가 아니다. 자신이 상대적으로 약자이기 때문에, 전략적으로 전투 위치를 재설정하는 것이다. 좁은 복도같은 곳에 숨어 그들이 다가오는 것을 기다렸다가, 때가 되면 자신의 모든 힘을 무기에 실어 적들을 기습하라.

마법사 중에서도, 강령술사나 소환사는 어둠의 부하들을 소환하여 당신을 압도하려 할 것이다. 이 때는 소환수를 무시하고, 마법사부터 공격해야 한다. 위험한 방법이지만, 모든 고통을 의지로 견뎌내고, 마법사에게 다가가 그들이 마법으로 도망치기 전에 제압하라. 그들이 소환을 하기 전에 기절시키거나 혼절시키는 전략 역시 유효하다.

마법사들은 또한 다양한 저주를 걸어 당신을 공포에 질리게 만들 것이다. 이를 이겨내기 위해, 몸에 자연의 힘을 주입할 것을 추천한다. 야생의 힘을 주입하면 저 끔찍한 흑마술을 추종하는 자들의 저주를 제거할 수 있으며, 재생의 힘을 주입하면 힘든 전투에서 큰 도움이 된다.

그들을 공격할 때 자비를 보이지 말라. 그들은 다른 사람들에게 자비를 베푼 적이 없다.
