"팀의 몇명이 이 임무를 진지하게 받아들이지 않는다는것이 두렵습니다. 구원자께서 저희에게 내려주신 책임감을 알기나 하는걸까요? 우리는 이 먼 땅에서 그분의 손이자 꼬리입니다. 그리고 우리에게 막강한 힘을 가져다 줄 먼 관문을 지키는 것이 우리의 임무입니다. 저희는 오래 전 우리를 멸망에 이르게 했던 자들의 혈육들에 맞서 싸울 첫번째 전선입니다. 그리고 이러한 노력들로, 우리 종족은 구원자께서 우리를 위해 계획하신 밝은 미래를 향한 길을 통해 새로운 지평으로 뻗어나가야 합니다. 슬라슐 만세! 포식자의 전설 만세!"]],
