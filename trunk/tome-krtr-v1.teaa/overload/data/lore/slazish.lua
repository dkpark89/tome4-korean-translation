"나의 동료 나가들아! 우리의 위대한 신전에서 멀리 떠나온 너희들을 질투하지 않는다. 하지만 너희들은 육지를 걸어다니는 놈들을 침입하기 위한 전초기지를 세우는 영광스러운 임무를 위해 뽑힌 자들이다. 그자들은 우리를 버리고 우리 종족을 죽음에 이르게 한 자들의 친척이자 후손들이다. 우리가 수세기동안 파도 밑에 숨어사는 동안, 저들은 의기양양하게 해를 숭배하고있다! 그들의 해가 곧 질것이고, 육지와 바다의 지배자로써 우리의 여명이 떠오를것이다.

"오크 대신에 이 전초기지를 공격한다고 실망하지 말거라. 나의 책략은 조심스럽게 계획되었고, 태양의 장벽은 나의 계획이 더 이상 유지되기 힘들정도로 큰 위협이다. 오크들은... 짧은 시간동안 써먹을 수 있을거다. 그러나 믿음을 가지거라. 우리의 때가 오는 그날 우리와 동등하게 서 있는 자들은 없을테니까. 우리의 위대함은 막을 수 없으며 가라앉지도 않을것이다! 우리의 오랜 고통의 역사가 마침내 밖으로의 구원을 이끌것이다!

"너희들의 즉각적인 임무는 명백하다 제군들. 관문이 제대로 설치되었고 안전한지 확인하라. 하지만 조심하도록, 쉐르'툴 마법 사용은 아직 실험단계이니 말이다. 그리고 지역을 정찰하고 주변을 요새화하되 비밀리에 하도록 하라. 임무가 잘 끝난다면 나, 그대들의 변변찮은 지도자인 슬라슐이 전선 최전방에 그대들과 함께 참여할 영광을 누리겠다. 그때까지는 조심해서 헤엄치도록 나의 형제 자매들이여, 우리의 영광을 잊지 말도록."
]],
