	lore = [[#{italic}#남쪽스파의 가장 위대했던 지배자에 대한 연구#{normal}#

장담하는데, 당신은 남쪽스파에 대해 들어본 적이 없을 것이다.

...모르는 것이 당연하다. 남쪽스파는 매혹의 시대에서 황혼의 시대까지만 존재했으며, 타르'에이알 해안가의 고립된 섬에 있던 아주 작은 왕국이었기 때문이다.

소문에 의하면, 남쪽스파는 살기 좋은 곳이었다고 한다. 기후 조건은 적절했고, 육지에 있던 인간들의 왕국과는 좋은 관계 속에서 교역이 이루어졌으며, 운 좋게도 이 왕국의 지하에는 엄청난 가치를 지닌 금속인 스트라라이트가 대량으로 존재하기까지 하였다.

그러나 이런 좋은 환경 속에서도 불구하고, 남쪽스파는 평범한 왕국의 위치를 벗어나지 못하였다. 왜냐고? 이 나라의 왕 덕분이었다. 시간의 안개 속에 잊혀져 관직에 있는 사람이나 노인들조차도 이름을 알지 못하는 그 왕은, 남쪽스파를 잘못 통치해 나라를 거의 멸망 직전까지 끌고 갔다. 철의 왕좌의 드워프들은 그 섬에 스트라라이트가 있다는 것을 듣고, 왕을 설득하여 그것을 헐값에 팔도록 유도하였다. 또한 남쪽스파의 군사력은 아주 미약한 수준이었음에도 불구하고, 왕은 하플링과 수많은 대규모 전투를 벌이고 있는 인간 왕국에 원군을 아낌없이 지원해주었다. 부실한 장비와 훈련되지 않은 원군은 강력한 하플링 부대의 힘 앞에서는 벌레만도 못한 존재였을 뿐만 아니라, 그 무모한 공격은 하플링들이 남쪽스파에 관심을 가지게 만들었다. 과도한 원군 지원으로 인해 나라에 병력이 거의 없어졌을 때, 하플링들의 침략은 시작되었다.

남은 기록에 의하면, 왕은 자신의 왕국이 몰락해가는 것을 혼란스러워했다고 한다. 마지막까지 자신이 행했던 모든 행동들이 옳았던 것이라고 믿으면서, 그는 하플링들의 침략이 시작된 지 얼마 되지 않아 사망하였다. 사인이 자살이었는지, 암살이었는지, 그저 노환이었는지는 아무도 모른다. 왕은 결혼하지 않았고 아이도 없었으며, 그의 왕위를 물려받을 혈육이라고는 그의 먼 사촌이며 드레이케라는 이름을 가진, 젊은이 하나 뿐이었다.

그리하여 드레이케는 왕위에 오르게 되었고, 곧 남쪽스파의 황금기가 시작되었다.

#{bold}#1. 드레이케와 하플링 무리.#{normal}#

새롭게 왕위에 오른 드레이케는, 현 시점에서 가장 명확하고 즉각적인 위협은 하플링의 늘어만 가는 침략이라고 보았다. 이것을 해결하기 위해, 그는 남쪽스파의 군대를 전면적으로 재조직하기로 하였다. 그가 볼 때, 오합지졸 무리인 남쪽스파의 군인들이 저 하플링들보다 유리한 점은 단 하나였다. 바로 이 섬의 지리에 대해 통달해있다는 것이었다. 그는 군대 조직을 작지만 파괴적인 살인병기와도 같은 부대로 만들었으며, 지리에 대한 이점을 살려 언제나 은밀하게 다니고, 최소한의 무장만 하며, 창과 검보다는 갑옷을 종잇장처럼 뚫어버릴 수 있는 스트라라이트제 단검을 들고 다니게 하였으며, 하플링 부대가 방심하고 있을 때 기습 위주의 공격만을 하라고 하였다. 이렇게 만들어진 남쪽스파의 “도둑 부대” 는 성공적이었다. 하플링 부대의 숫자는 어마어마했지만, 드레이케 군의 지속적인 기습공격에 하플링 군대의 사기는 급속히 떨어졌다. 마침내, 하플링 군대는 남쪽스파에 “상종할 가치도 없다” 는 말을 남기고 철수하였다.

남쪽스파는 이 평화와 새롭게 만들어진 군대의 힘을 기리는 축제를 열었다. 그러나 드레이케에게 있어, 이 즐거움은 오래가지 않았다. 도둑 부대의 단검과 갑옷을 만드는 스트라라이트는 여전히 드워프들의 주머니 속에 있었기 때문에, 아직 행복을 느끼기에는 멀었다고 생각한 것이다. 드레이케는 한가지 계획을 세웠다. 이 계획이 실행되기 전까지는, 드워프들이 우리보다 더 행복해하고 있을 것이다.

#{bold}#2. 드레이케와 스트라라이트 탈환계획 #{normal}#

드워프들은 언제나 비밀스러운 종족이었다. 지금도 그들이 사는 곳인 “철의 왕좌” 의 위치를 아는 사람의 숫자는 그리 많지 않으며, 더욱이 남쪽스파가 있던 시대의 사람들은 드워프를 그저 신화 속의 존재일 뿐이라고까지 생각하였다. 엄청난 양의 스트라라이트는 그들의 비밀스러운 문을 열기에 충분했지만...

드레이케는 철의 왕좌에 직접 발을 옮겼다. 이 소식을 들은 그곳의 드워프들은 아마 새로운 왕이 지금까지 스트라라이트를 지나치게 싼 값에 구매해왔던 것에 대한 가벼운 사과와 앞으로 스트라라이트를 정상적인 가격에 구매해줄 것을 요청하리라고 예상하였으며, 그것을 수용할 준비를 하고 있었다.

하지만 현실은 그렇지 않았다. 과거의 무능력했던 남쪽스파의 왕은 사라졌으며, 드워프들이 마주한 것은 앞으로 스트라라이트의 가격을 20 배로 올릴 것을 선언한 젊고 냉엄한 왕이었다.

드워프들은 최대한 절제하며 이 당돌한 왕의 요구를 비웃었다. 최근 남쪽스파가 하플링 군대를 격퇴했다는 소식을 듣기는 했지만, 드워프들은 여전히 남쪽스파의 스트라라이트는 언제든지 무력으로 빼앗아올 수 있다고 생각하고 있었기 때문이다. 하지만 드레이케는 그들이 이 요구를 받아들일 수밖에 없는 방법을 이미 생각해왔으며, 이를 실행하기 위해 작은 주머니를 꺼내 들었다.

드레이케는 용가죽으로 만든 작은 주머니를 높게 들어올리더니, 안에 들어있던 내용물을 모조리 바닥에 쏟아내었다. 주머니 안에 있던 많은 양의 스트라라이트가 바닥의 먼지와 진흙과 잡석과 섞여, 쓸모 없고 가치 없는 쇳덩어리가 되고 말았다. 드워프들은 이 지독한 낭비에 기겁하였으며, 소문이 사실이라면 이 자리에서 졸도한 드워프마저 있었다. 드레이케는 그들에게, 만약 자신의 요구가 받아들여지지 않는다면 그의 왕국에 묻혀 있는 모든 스트라라이트도 지금과 같은 운명을 맞이하게 될 것이라고 소리 높여 외쳤다.

드레이케가 철의 왕좌를 떠나고 난 뒤, 드워프들은 이전보다 스트라라이트의 가격을 30 배 더 올리는 것에 동의하였다.
