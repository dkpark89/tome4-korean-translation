	lore = [[#{bold}#3. 드레이케와 은둔한 마법사들#{normal}#

더 이상 외세의 침략은 없었으며, 금고는 드워프들이 지불한 금으로 빠르게 차오르기 시작하였다. 많은 사람들이 남쪽스파의 행운은 이 이상 커질 수 없을 것이라고 생각하였다. 하지만 드레이케에게는 하나의 과제가 더 남아있었다. 바로 ‘은둔자’ 들에 대한 문제였다.

오랜 세월 동안, 남쪽스파에는 소수의 은둔한 마법사들이 살아오고 있었다. 이전 왕의 무능함과 이들이 외딴 지역에 산다는 것은, 이들이 대륙에서는 금지된 마법의 연습과 실험을 할 수 있게 된다는 것을 의미하였다. 자연 속에서 은둔하고 있는 그들 중에, 새로운 왕에 대한 소식을 들은 사람은 몇 명 되지 않았다. 대부분은 이 소식에 신경조차 쓰지 않았다. 드레이케의 친위대는 이 마법사들이 살고 있던 집의 문을 박차고 들어가, 새로운 왕에 충성을 맹세하고 왕을 도울 것을 요구하였다. 드레이케는 그들에게 최초이자 최후통첩을 내렸다.

“이곳은 남쪽스파 왕국이다. 바로 나의 왕국이다. 내 왕국에 있는 모든 것들은 나의 통제권 아래에 있다. 나의 국민, 나의 대지, 나의 자원, 그리고 나의 마법 역시 마찬가지이다. 남쪽스파에 계속 살고 싶다면, 한 가지 조건이 있다. 내 왕국의 일원이 되어, 나의 명령에 복종하며, 왕국의 발전을 위해 마법을 사용할 것이 바로 그 조건이다. 필요할 때를 제외하면, 계속 은둔을 하건 말건 그것은 너희들의 자유이다. 하지만 너희들의 충성심은 나의 것이여야만 한다. 나를 따르겠는가?”

은둔한 마법사들의 반응? 그들은 웃었다. 하지만 이 웃음은 드워프들이 지었던 비웃음이 아닌, 믿을 수 없다는 웃음이었다. 기록에 의하면, 마법사들은 이렇게 대답하였다.

“드레이케 왕이시여! 당신은 이런 딱딱한 최후통첩 따위로 우리를 겁먹게 할 필요가 없습니다! 그저 간단한 요청 하나면 충분했을 것입니다. 사실, 지금까지 우리들은 단순히 사람들의 눈에 띄지 않는다는 이유 하나만으로 이곳에서 살고 있었습니다. 하지만 이제는 남쪽스파의 떠오르는 별을 무시할 수가 없군요. 왕국의 영광은 계속될 것이며, 우리는 그 영광의 일부가 될 것입니다. 당신을 따르겠습니다.”

