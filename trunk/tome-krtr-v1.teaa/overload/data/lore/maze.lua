  사냥을 하는 동안 날 버티게 해줄 끝내주는 약병들을 많이 준비두었다. 내년 이맘때쯤이면 왕이랑 필적할 정도의 전리품 콜렉션을 모을수 있을거야!

  그런데 하나를 잘못 설치한것 같다... 이 근처인게 분명한데.


